import Foundation

/**
 Перечисление `CellContentAlignment` определяет возможные варианты выравнивания контента в ячейке.

 - Cases:
    - top: Контент выравнивается по верхней границе ячейки.
    - center: Контент выравнивается по центру ячейки.
    - bottom: Контент выравнивается по нижней границе ячейки.
 */
public enum CellContentAlignment {
    case top
    case center
    case bottom
}
